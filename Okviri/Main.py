l1=""
l2=""
l3=""
l4=""
l5=""

num=1

n=input()
for i in n:
    mod=num%3
    mod1=(num+1)%3
    mod2=(num-1)%3
    if num==1:
        l1+="..#.."
        l2+=".#.#."
        l3+="#."+i+".#"
        l4+=".#.#."
        l5+="..#.."
    elif mod==0:
        l1+="..*.."
        l2+=".*.*."
        l3+="*."+i+".*"
        l4+=".*.*."
        l5+="..*.."
    elif mod!=0:
        if mod1==0: #after            
            l1+=".#."
            l2+="#.#"
            l3+="."+i+"."
            l4+="#.#"
            l5+=".#."
        elif mod2==0: #before
            l1+=".#.."
            l2+="#.#."
            l3+="."+i+".#"
            l4+="#.#."
            l5+=".#.."
            
    num+=1


if mod1==0:
    l1+="."
